## CREAR UNA TUPLA CON ELEMENTOS E IMPRIMIRLOS ##

my_tuple = (3, False, True, 'tres', 10.1, 'cuarenta')

print(f'''
      Datos en tupla:
      {my_tuple}
      ''')

# Para verificar que es una tupla podemos utilizar el metodo 'type()'
print("Verificación de clase de lista -> ", type(my_tuple))
# El type() se puede aplicar a cualquier lista que queramos saber que clase és.
