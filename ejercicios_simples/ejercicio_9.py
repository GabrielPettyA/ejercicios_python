## REALIZAR LA POTENCIACIÓN DE UN NÚMERO ##

base_num = 4
exp_num = 2

result_potent = base_num ** exp_num

print("Resultado de la potenciación de un número: ", result_potent)

# De igual manera, se puede realizar los ingresos de los números a través de la terminal.
base_num = int(input("Ingrese Base: "))
exp_num = int(input("Ingrese Exponente: "))

result_potent = base_num ** exp_num

print(f'''
----------------------------------------------------
  Potencia de Base {base_num} con Exponente {exp_num} = {result_potent}
----------------------------------------------------
''')


