## PASAR UNA CADENA EN MINÚSCULA A MAYÚSCULA ##

cadena_minuscula = "hola python"
cadena_mayuscula = cadena_minuscula.upper()

print("Cadena en Mayúscula -> ", cadena_mayuscula)

# También se puede realizar de mayúscula a minúscula.

nuevamente_minuscula = cadena_mayuscula.lower()
print("Pasando nuevamente a minúscula -> ", nuevamente_minuscula)

# O se puede realizar directamente al imprimir el resultado.
print("En minúscula en el print -> ", nuevamente_minuscula.lower())
print("En Mayúscula en el print -> ", cadena_mayuscula.upper())



